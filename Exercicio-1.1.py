print('Execucção de Expresões Aritméticas com Multiplos Operandos')
A = 10
B = 25
C = 7

R = 2 * A + B
print("Resultado da operacao:", R)

R = 2 * (A + B)
print("Resultado da operacao:", R)

X = (A + B) / (A + C)
print("Resultado da operacao:", X)

X = (2 * B - C) / (2 * A)
print("Resultado da operacao:", X)

X = -A * 2 + B
print("Resultado da operacao:", X)

X = A % B * C
print("Resultado da operacao:", X)


